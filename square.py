# Method 1: Using exponentiation operator (**)
num = float(input("Enter a number: "))
sqrt = num ** 0.5
print("The square root of", num, "is", sqrt)


# Method 2: Using math.sqrt()
import math

num = float(input("Enter another number: "))
sqrt = math.sqrt(num)
print("The square root of", num, "is", sqrt)
